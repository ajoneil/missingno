pub fn fill(buf: &mut [u8]) {
    assert_eq!(buf.len(), WRAM_SIZE);

    // First 2 KiB: 256-byte stripes 0x00 / 0xFF.
    let mut addr = 0x0000;
    while addr < 0x0800 {
        buf[addr..addr + STRIPE].fill(0x00);
        buf[addr + STRIPE..addr + 2 * STRIPE].fill(0xFF);
        addr += 2 * STRIPE;
    }
    // Next 2 KiB: stripes inverted (0xFF / 0x00).
    while addr < HALF {
        buf[addr..addr + STRIPE].fill(0xFF);
        buf[addr + STRIPE..addr + 2 * STRIPE].fill(0x00);
        addr += 2 * STRIPE;
    }
    // Upper 4 KiB mirrors the lower 4 KiB.
    buf.copy_within(0..HALF, HALF);

    for &(offset, value) in DUMP_DIFF {
        buf[offset as usize] = value;
    }
}
